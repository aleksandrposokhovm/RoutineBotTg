#!/bin/bash

# Скрипт автоматического деплоя RoutineBot на сервере с использованием PM2
# Скрипт завершит работу, если любая команда вернет ошибку (set -e)
set -e

echo "🚀 Начинаем процесс обновления RoutineBot..."

# 1. Получение актуального кода из репозитория
echo "📥 Получение свежего кода из git..."
git pull

# 2. Установка зависимостей бэкенда
echo "📦 Установка зависимостей бэкенда..."
cd backend
npm install

# 3. Применение схемы базы данных и генерация Prisma-клиента
echo "🗄️ Подготовка базы данных (Prisma)..."
npx prisma generate
npx prisma db push

# 4. Сборка TypeScript бэкенда
echo "🛠️ Сборка бэкенда..."
npm run build

# 5. Установка зависимостей и сборка фронтенда
echo "📦 Установка зависимостей и сборка фронтенда..."
cd ../frontend
npm install
npm run build

# 6. Перезапуск приложения в PM2
echo "🔄 Перезапуск процесса в PM2..."
cd ..
# Перезапустит или запустит приложение, если оно еще не в PM2
pm2 startOrReload ecosystem.config.js --update-env

echo "✅ Деплой успешно завершен! RoutineBot запущен и работает."
